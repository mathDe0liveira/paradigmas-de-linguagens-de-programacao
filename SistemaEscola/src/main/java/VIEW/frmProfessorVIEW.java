    private void CarregarCampos() {
        int setar = tblProfessor.getSelectedRow();
        
        txtId.setText(tblProfessor.getModel().getValueAt(setar, 0).toString());
        txtMatricula.setText(tblProfessor.getModel().getValueAt(setar, 1).toString());
        txtNome.setText(tblProfessor.getModel().getValueAt(setar, 2).toString());
        txtCpf.setText(tblProfessor.getModel().getValueAt(setar, 3).toString());
        txtSenha.setText(tblProfessor.getModel().getValueAt(setar, 4).toString());
        txtFormacao.setText(tblProfessor.getModel().getValueAt(setar, 5).toString());
        txtDisciplina.setText(tblProfessor.getModel().getValueAt(setar, 6).toString());
    }
    
