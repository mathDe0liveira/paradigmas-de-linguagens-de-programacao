    private void cadastrarProfessor(){
        int id, matricula;
        String nome, cpf, senha, formacao, disciplina;
        
        id = Integer.parseInt(txtId.getText());
        matricula = Integer.parseInt(txtMatricula.getText());
        nome = txtNome.getText();
        cpf = txtCpf.getText();
        senha = txtSenha.getText();
        formacao = txtFormacao.getText();
        disciplina = txtDisciplina.getText();
        
        ProfessorDTO objprofessordto = new ProfessorDTO();
        objprofessordto.setId(id);
        objprofessordto.setMatricula(matricula);
        objprofessordto.setNome(nome);
        objprofessordto.setCpf(cpf);
        objprofessordto.setSenha(senha);
        objprofessordto.setFormacao(formacao);
        objprofessordto.setDisciplina(disciplina);
        
        ProfessorDAO objprofessordao = new ProfessorDAO();
        objprofessordao.cadastrarProfessor(objprofessordto);
    }
    
    private void listarProfessores(){
        ProfessorDAO objprofessordao = new ProfessorDAO();
        DefaultTableModel model = (DefaultTableModel) tblProfessor.getModel();
        model.setNumRows(0);
        ArrayList<ProfessorDTO> lista = objprofessordao.pesquisarProfessor();
        for(int num = 0; num < lista.size(); num++) {
            model.addRow( new Object[] {
                lista.get(num).getId(),
                lista.get(num).getMatricula(),
                lista.get(num).getNome(),
                lista.get(num).getCpf(),
                lista.get(num).getSenha(),
                lista.get(num).getFormacao(),
                lista.get(num).getDisciplina()
            });
        }
    }
