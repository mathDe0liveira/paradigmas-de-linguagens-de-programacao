    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int id, matricula;
        String nome, cpf, senha, ano;
        
        id = Integer.parseInt(txtID.getText());
        matricula = Integer.parseInt(txtMatricula.getText());
        nome = txtNome.getText();
        cpf = txtCPF.getText();
        senha = txtSenha.getText();
        ano = txtAno.getText();
        
        AlunoDTO objalunodto = new AlunoDTO();
        objalunodto.setId_Aluno(id);
        objalunodto.setMatricula_Aluno(matricula);
        objalunodto.setNome_Aluno(nome);
        objalunodto.setCpf_Aluno(cpf);
        objalunodto.setSenha_Aluno(senha);
        objalunodto.setAno_Aluno(ano);
        
        AlunoDAO objalunodao = new AlunoDAO();
        objalunodao.cadastrarAluno(objalunodto);
    }//GEN-LAST:event_btnCadastrarActionPerformed

